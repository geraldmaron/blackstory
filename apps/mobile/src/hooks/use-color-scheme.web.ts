import { useEffect, useState } from 'react';
import { useColorScheme as useRNColorScheme } from 'react-native';

/**
 * To support static rendering, this value needs to be re-calculated on the client side for web
 */
export function useColorScheme() {
  const [hasHydrated, setHasHydrated] = useState(false);

  useEffect(() => {
    // Deliberate one-time hydration flag (client only re-render after SSR),
    // not a synchronization loop — the standard Expo static-rendering
    // pattern this file's docblock describes.
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setHasHydrated(true);
  }, []);

  const colorScheme = useRNColorScheme();

  if (hasHydrated) {
    return colorScheme;
  }

  // Dark-first default while static HTML hydrates (matches resolveThemeName).
  return 'dark';
}
